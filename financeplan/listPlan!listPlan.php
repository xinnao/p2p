	        		<p style="font-weight:bold">1、人人贷推出的优选理财计划是类似银行的理财产品吗？</p>
	        		<p>人人贷优选理财计划是以人人贷现有各类投资标的为基础，为更加便于用户投标、提高回款利用率、解决资金流动性问题而推出的投标计划。它更类似于一种高级投标工具，以用户认可的既定规则为加入计划的用户自动投标。首先，用户对于参加计划的资金使用情况、计划内投标情况都可以清晰掌握，且每个投标均有其对应的借款合同，这是与理财产品完全不同的。其次，理财计划用户可以每月定时提取计划内投标所得利息，每位用户的利息所得取决于各自的计划内投标情况而不尽相同，这一点也与理财产品有本质区别。</p>
	        		
